package Loops;

public class Nested {

//     For Understading Check Breakpoints  

    public static void main(String[] args) {
        for(int i = 0;i<=3;i++)
        {
            for(int j = 1;j<=3;j++)
            {
                System.out.println();
            }
        }
        System.out.println();

        // Total 12
    }
}
